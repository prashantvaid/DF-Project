def classify_image(image_path, model_path):
    try:
        # Load model
        device = torch.device('cpu')  # Use CPU for deployment
        model = torch.load(model_path, map_location=device, weights_only=False)
        model.eval()
        
        # Get actual number of classes from model
        with torch.no_grad():
            dummy_input = torch.randn(1, 3, 224, 224)
            dummy_output = model(dummy_input)
            num_classes = dummy_output.shape[1]
        
        # Image preprocessing
        transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
        ])
        
        # Load and preprocess image
        image = Image.open(image_path).convert('RGB')
        image_tensor = transform(image).unsqueeze(0)
        
        # Make prediction
        with torch.no_grad():
            outputs = model(image_tensor)
            probabilities = torch.nn.functional.softmax(outputs[0], dim=0)
            confidence, predicted = torch.max(probabilities, 0)
            
        result = {
            "prediction": CLASS_NAMES[predicted.item()],
            "confidence": float(confidence.item() * 100),
            "all_predictions": {
                CLASS_NAMES[i]: float(prob * 100) 
                for i, prob in enumerate(probabilities)
            }
        }
        
        return result
        
    except Exception as e:
        return {"error": str(e)}

if __name__ == "__main__":
    if len(sys.argv) != 3:
        print(json.dumps({"error": "Usage: python classify_image.py <image_path> <model_path>"}))
        sys.exit(1)
    
    image_path = sys.argv[1]
    model_path = sys.argv[2]
    
    result = classify_image(image_path, model_path)
    print(json.dumps(result))